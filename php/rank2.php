<div class="detail01 clearfix">
<h4>Conoha Wing</h4>
<p class="right"><img src="/img/cap_conoha.png" alt="Conoha Wing" /></p>
<p>
Webページ表示がNo1となっているのがConohaWingになります。
ワードプレスにあったWebサーバーソフトである「nginx」を搭載。
技術面にもこだわりがあり「HTTP2」や「LiteSpeed LSAPI」を採用しています。
それでいて独自ドメインがずっと無料（Wingプラン）だったり、
初期料金が0円となっているため、料金もお得なレンタルサーバーになっています。
