<div class="detail01 clearfix">
<h4>ＷＩＸ</h4>
<p class="right"><img src="/image/cap_wix.png" alt="ＷＩＸ" /></p>
<p>
かんたんホームーページ作成サービスのＷＩＸです。その名前のとおりHTMLやCSSの知識なしにホームページが作れます。
100種類以上のおしゃれなテンプレートから選択。細かなデザインはドラック＆クリックで変更可能で、さらにスマホにも自動対応。
まったくの初心者でも3日あればホームページが作れます。<br>
広告が表示ありの無料コースから利用できるのが魅力で、ドメイン取得コースや広告なしのコース利用の場合は月額500円～1000円以上の料金が目安となります。

</p>
